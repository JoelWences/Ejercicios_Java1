package Seccion3PrimitiveTypes;

public class primitivoTipoChar {
    public static void main(String[] args) {
        char miCaracter = 'a';
        System.out.println("Mi caracter = "+miCaracter);

        var varChar1 = '\u0021';
        System.out.println("Mi caracter = "+varChar1);

        var varChardDecimal2 = (char)33;
        System.out.println("Mi caracter = "+varChardDecimal2);

        var varCharSimbiolo3 = '!';
        System.out.println("Mi caracter = "+varCharSimbiolo3);

        int varibaleenterasimbolo = '!';
        System.out.println("Mi caracter = "+varibaleenterasimbolo);

        int letra = 'a';
        System.out.println("Mi caracter = "+letra);
    }
}
